void basic06()
{
	//循环结构
	//3、for循环语句
	//语法： for(起始表达式;条件表达式;末尾循环体){循环语句;}
	// ps：起始表达式当作0，条件表达式当作1，循环语句当作2，末尾循环体当作3
	//注意：起始表达式只执行一次
	//for循环执行顺序：0123123123......

	//例子：打印数字 0~9
	/*for (int i = 0; i < 10; i++)
	{
		cout << i << endl;
	}*/

	//例子2：明7暗7
	for (int i = 1; i <= 100; i++)
	{
		if ( i % 10 == 7 || i / 10 == 7) //i对10取模得到个位，个位有7要敲；i除以10得到十位，十位有7也要敲；
		{
			cout << "敲桌子" << endl;
		}
		else
		{
			cout << i << endl;
		}
	}
}